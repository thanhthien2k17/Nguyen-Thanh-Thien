    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = tblBank.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("view/client/Resource").getString("CHOOSE ROW"));
        } else {
            String number = (String) tblBank.getValueAt(row, 0);
            try {
                if (client.delete(number)) {
                    JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("view/client/Resource").getString("SUCCESS"));
                    loadTable(client.readAll());
                } else {
                    JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("view/client/Resource").getString("FALSE"));
                }
            } catch (RemoteException ex) {
                Logger.getLogger(BankMainFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

