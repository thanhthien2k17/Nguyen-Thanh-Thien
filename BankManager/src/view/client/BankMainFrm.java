    void loadTable(List<BankDTO> list) {
        Vector cols = new Vector();
        cols.add(java.util.ResourceBundle.getBundle("view/client/Resource").getString("NUMBER"));
        cols.add(java.util.ResourceBundle.getBundle("view/client/Resource").getString("NAME"));
        cols.add(java.util.ResourceBundle.getBundle("view/client/Resource").getString("ADDRESS"));
        Vector rows = new Vector();
        for (BankDTO e : list) {
            Vector row = new Vector();
            row.add(e.getNumber());
            row.add(e.getName());
            row.add(e.getAddress());
            rows.add(row);
            tblBank.setModel(new DefaultTableModel(rows, cols));
            tblBank.updateUI();
            spnBank.setViewportView(this.tblBank);
        }
    }

