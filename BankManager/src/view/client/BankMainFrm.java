    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        BankDTO p = new BankDTO();
        String s = txtNumber.getText().trim();
        try {
            if (client.checkNumber(s)) {
                p.setNumber(txtNumber.getText().trim());
                p.setName(txtName.getText().trim());
                p.setAddress(txtAddress.getText().trim());
                try {
                    if (client.create(p) != null) {
                        JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("view/client/Resource").getString("ACCESS!"));
                        loadTable(client.readAll());
                    } else {
                        JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("view/client/Resource").getString("FALL!"));
                    }
                } catch (RemoteException ex) {
                    Logger.getLogger(BankMainFrm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("view/client/Resource").getString("NUMBER ILLEGAL!"));
            }
        } catch (RemoteException ex) {
            Logger.getLogger(BankMainFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

