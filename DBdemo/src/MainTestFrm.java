    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String code = txtCode.getText().trim();
        String name = txtName.getText().trim();
        int age = Integer.parseInt(txtAge.getText().trim());
        float salary = Float.parseFloat(txtSalary.getText().trim());
        String username = txtUsername.getText().trim();
        Employee s = new Employee(code, name, age, salary, username, 1);
        if (ac.creater(s) != null) {
            JOptionPane.showMessageDialog(this, "Success");
        } else {
            JOptionPane.showMessageDialog(this, "Fail");
        }
        loadTable(ac.readAll());
    }//GEN-LAST:event_btnCreateActionPerformed

    private void tblEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeeMouseClicked
        // TODO add your handling code here:
        int row = tblEmployee.getSelectedRow();
        txtCode.setText((String) tblEmployee.getValueAt(row, 0));
        txtCode.setEditable(false);
        txtName.setText((String) tblEmployee.getValueAt(row, 1));
        txtAge.setText(String.valueOf(tblEmployee.getValueAt(row, 2)));
        txtSalary.setText(String.valueOf(tblEmployee.getValueAt(row, 3)));
        txtUsername.setText((String) tblEmployee.getValueAt(row, 4));
    }//GEN-LAST:event_tblEmployeeMouseClicked

