    void loadTable(List<Employee> list) {
        Vector colum = new Vector();
        colum.add("code");
        colum.add("name");
        colum.add("age");
        colum.add("salary");
        colum.add("username");

        Vector rows = new Vector();
        for (Employee s : list) {
            Vector row = new Vector();
            row.add(s.getCode());
            row.add(s.getName());
            row.add(s.getAge());
            row.add(s.getSalary());
            row.add(s.getUsername());
            rows.add(row);
        }
        tblEmployee.setModel(new DefaultTableModel(rows, colum));
        tblEmployee.updateUI();
        spnEmployee.setViewportView(this.tblEmployee);

    }

