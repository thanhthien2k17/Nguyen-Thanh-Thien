    private void btnFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterActionPerformed
        try {
            Date from = txtFrom.getDate();
            Date to = txtTo.getDate();
            if (from != null && to != null) {
                from = new SimpleDateFormat("dd/MM/yyyy").parse(new SimpleDateFormat("dd/MM/yyyy").format(from));
                to = new SimpleDateFormat("dd/MM/yyyy").parse(new SimpleDateFormat("dd/MM/yyyy").format(to));
                loadTable(invoi.readByDate(from, to));
            } else {
                JOptionPane.showMessageDialog(this, "Select from and to");
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Select day from and to");
        }
    }//GEN-LAST:event_btnFilterActionPerformed

