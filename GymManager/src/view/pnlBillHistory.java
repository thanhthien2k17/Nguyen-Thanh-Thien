private void loadTable(List<InvoiceidDTO> readAll) {
        Vector cols = new Vector();
        cols.add("ID");
        cols.add("Users");
        cols.add("Date");
        cols.add("Price");
        cols.add("CustomerId");
        cols.add("CourseId");

        Vector rows = new Vector();
        for (InvoiceidDTO i : readAll) {
            Vector row = new Vector();
            row.add(i.getId());
            row.add(users.readById(i.getUserid()).getUsername());
            row.add(new SimpleDateFormat("dd/MM/yyyy").format(i.getDatetime()));
            row.add(i.getTotal());
            for (BillsDTO j : bill.readAll()) {
                if (j.getInvoiceid() == i.getId()) {
                    row.add(j.getCustomerid());
                    row.add(j.getCourseid());
                    break;
                }
            }
            rows.add(row);
        }

        tblHistoryBill.setModel(new DefaultTableModel(rows, cols));
        tblHistoryBill.updateUI();
        spnHistoryBill.setViewportView(this.tblHistoryBill);
    }
