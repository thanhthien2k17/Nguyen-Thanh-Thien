package entity;

/**
 *
 * @author thien
 */
public class Good {

    public int code;
    public String name;

    public Good() {
    }

    public Good(int code, String name) {
        this.code = code;
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
